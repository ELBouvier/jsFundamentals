// Assignment Operators

let x = 5;

// Addition assignment
x += 1; // x = x + 1

// Subtraction assignment
x -= 1; // x = x - 1

// Multiplication Assignment
x *= 1; // x = x * 1

// Division Assignment
x /= 1; // x = x / 1

// Remainder (modulus) Assignment
x %= 1 // x = x % 1

// Exponential Assignment
x ** 1 // x = x ** 1 (** = ^)

let x = 3;
console.log(x ** 2);