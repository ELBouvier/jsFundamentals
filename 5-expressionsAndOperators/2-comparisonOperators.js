// Comparison Operators

// Equal- Checks to see that the value is the same
// ==
'3' == 3;

// Strictly Equal- Checks to see if the data type and value are the same.
// ===
3 === 3;

// Not equal
// !=
'3' != 4;

// Strict not equal
// !==
'3' !== 3;

// Greater than
// >
3 > 2;

// Less than
// <
2 < 3;

// Greater than or equal to
// >=
3 >= 2;

// Less than or equal to
// <=
2 <= 3;

// And (simultaneous match)
// &&
2 && 3;

// Or (either one)
// ||
2 || 3;